    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (!bank.isEmpty()) {
    // 获取下一个顾客对象
    Customer nextCustomer = bank.frontElement();
    
    // 将 double 类型数据转换为 String
    String amount = String.valueOf(nextCustomer.getSavings());
    
    // 组合顾客信息字符串
    String customerInfo = "Name: " + nextCustomer.getName() + ", Savings: $" + amount+",Date:"+nextCustomer.getDate();
    
    // 在文本框中显示下一个顾客信息
    jTextArea1.setText("Next Customer:\n" + customerInfo);
    
    // 出队列操作
    bank.dequeue();
} else {
    // 如果没有预约客户，显示提示信息
    jTextArea1.setText("No more appointments.");
}
    }//GEN-LAST:event_jButton5ActionPerformed

