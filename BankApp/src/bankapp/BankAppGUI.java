    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String name = jTextField1.getText();
            double savings = Double.parseDouble(jTextField2.getText());
            String date = jTextField3.getText();
            bank.enqueue(name, savings, date);
            JOptionPane.showMessageDialog(null, "The Customer's message was added successfully.");

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Please enter a valid priority number.");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        List<Customer> customers = bank.listAllCustomers();

        // 在文本区域中显示客户信息
        jTextArea1.setText("Customers:\n");
        for (Customer customer : customers) {
            jTextArea1.append(customer.getName() + ", Savings: $" + customer.getSavings() + ",Date:" + customer.getDate() + "\n");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

